package aoc2018.helper;

public class SpaceTimeCoordinate {

    private final int a;
    private final int b;
    private final int c;
    private final int d;

    public SpaceTimeCoordinate(int a, int b, int c, int d) {
        this.a = a;
        this.b = b;
        this.c = c;
        this.d = d;
    }

    public int getA() {
        return a;
    }

    public int getB() {
        return b;
    }

    public int getC() {
        return c;
    }

    public int getD() {
        return d;
    }
}
