package aoc2018.helper;

public class Cube {
    private long numBots;
    private long edgeLength;
    private long distance;

    private long x;
    private long y;
    private long z;

    public long getNumBots() {
        return numBots;
    }

    public void setNumBots(long numBots) {
        this.numBots = numBots;
    }

    public long getEdgeLength() {
        return edgeLength;
    }

    public void setEdgeLength(long edgeLength) {
        this.edgeLength = edgeLength;
    }

    public long getDistance() {
        return distance;
    }

    public void setDistance(long distance) {
        this.distance = distance;
    }

    public long getX() {
        return x;
    }

    public void setX(long x) {
        this.x = x;
    }

    public long getY() {
        return y;
    }

    public void setY(long y) {
        this.y = y;
    }

    public long getZ() {
        return z;
    }

    public void setZ(long z) {
        this.z = z;
    }
}
